// Export all built-in themes
export const builtInThemes: ThemeConfig[] = [
  tokyoNightTheme,
  oneDarkTheme,
  githubLightTheme,
  draculaTheme,
  highContrastDarkTheme
]

export const getBuiltInTheme = (id: string): ThemeConfig | undefined => {
  return builtInThemes.find(theme => theme.id === id)
}

export const getBuiltInThemesByType = (type: 'light' | 'dark'): ThemeConfig[] => {
  return builtInThemes.filter(theme => theme.type === type)
}

export const getBuiltInThemesByTag = (tag: string): ThemeConfig[] => {
  return builtInThemes.filter(theme => theme.tags?.includes(tag))
}